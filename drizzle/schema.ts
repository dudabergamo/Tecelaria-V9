import { int, mysqlEnum, mysqlTable, text, timestamp, varchar, boolean, date } from "drizzle-orm/mysql-core";

/**
 * Core user table backing auth flow.
 */
export const users = mysqlTable("users", {
  id: varchar("id", { length: 36 }).primaryKey(),
  openId: varchar("openId", { length: 64 }),
  name: varchar("name", { length: 255 }),
  email: varchar("email", { length: 320 }).notNull().unique(),
  loginMethod: varchar("loginMethod", { length: 64 }),
  role: mysqlEnum("role", ["user", "admin"]).default("user").notNull(),
  
  // Tecelaria specific fields
  phone: varchar("phone", { length: 20 }),
  address: varchar("address", { length: 500 }),
  cep: varchar("cep", { length: 10 }),
  cpf: varchar("cpf", { length: 20 }),
  identityDocument: varchar("identityDocument", { length: 50 }),
  birthDate: date("birthDate"),
  password: varchar("password", { length: 255 }),
  profilePictureUrl: varchar("profilePictureUrl", { length: 500 }),
  howHeardAboutTecelaria: varchar("howHeardAboutTecelaria", { length: 100 }),
  emailConfirmed: boolean("emailConfirmed").default(false).notNull(),
  emailConfirmationCode: varchar("emailConfirmationCode", { length: 10 }),
  emailConfirmationCodeExpiresAt: timestamp("emailConfirmationCodeExpiresAt"),
  kitActivatedAt: timestamp("kitActivatedAt"),
  memoryPeriodEndDate: timestamp("memoryPeriodEndDate"),
  bookFinalizationEndDate: timestamp("bookFinalizationEndDate"),
  programEndDate: timestamp("programEndDate"),
  lastUploadDate: timestamp("lastUploadDate"),
  bookProcessingStarted: boolean("bookProcessingStarted").default(false).notNull(),
  bookReadyForReview: boolean("bookReadyForReview").default(false).notNull(),
  bookApproved: boolean("bookApproved").default(false).notNull(),
  customMemoryCount: int("customMemoryCount").default(0).notNull(),
  lastSignedIn: timestamp("lastSignedIn"),
  
  createdAt: timestamp("createdAt").defaultNow().notNull(),
  updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
});

export type User = typeof users.$inferSelect;
export type InsertUser = typeof users.$inferInsert;

